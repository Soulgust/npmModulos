const suma = function suma(num1, num2){
    return num1+num2
}
const resta = function resta(num1, num2){
    return num1-num2
}
const multiplacion = function multiplacion(num1, num2){
    return num1*num2
}
const dividir = function dividir(num1, num2){
    return num1/num2
}

module.exports ={
    suma,
    resta,
    multiplacion,
    dividir
}